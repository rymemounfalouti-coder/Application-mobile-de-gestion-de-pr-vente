    final code = locale.languageCode;
    return _translations.containsKey(code) ? code : 'fr';
  }

  String text(String key) {
    return _translate(key, languageCode);
  }

  static String globalText(String key) {
    final code = AppLocaleController.instance.languageCode;
    return _translate(key, code);
  }

  static String _translate(String key, String code) {
    if (key.startsWith('Hier,')) {
      return '${_translate('Hier', code)}${key.substring(4)}';
    }

    final literal = _literalTranslations[code]?[key];
    if (literal != null) return literal;

    final direct = _translations[code]?[key] ?? _translations['fr']?[key];
    if (direct != null) return direct;

    final frEntries = _translations['fr']!.entries;
    for (final entry in frEntries) {
      if (entry.value == key) {
        return _translations[code]?[entry.key] ?? entry.value;
      }
    }

    return key;
  }

